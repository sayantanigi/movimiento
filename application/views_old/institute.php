<script>
$(document).ready(function() {
    $('#sendMessage').click(function() {
        var fname = $('#fname').val();
        var lname = $('#lname').val();
        var email = $('#emailadd').val();
        var subject = $('#subject').val();
        var message = $('#message').val();
        if (fname == "" || lname == "" || email == "" || subject == "" || message == "") {
            $('#emailSentMsg').show().html('<p style="color:red">Please fill out all fields!</p>');
            setTimeout(() => {
                $('#emailSentMsg').hide()
            }, 3000);
        } else {
            $.ajax({
                url: '<?= base_url()?>Home/contactInstitute',  //server script to handle requests, defaults to the current page
                type: "POST",       //request method, POST or GET
                data:{fname : fname, lname : lname, email : email, subject : subject, message : message },
                success: function(response) {   //on receive of reply from server
                    if(response.status == "1") {
                        $('#emailSentMsg').show().html('<p style="color:green">Email Sent Successfully</p>');
                        setTimeout(() => {
                            location.reload();
                        }, 3000);
                    } else {
                        $('#emailSentMsg').show().html('<p style="color:red; text-align: center; margin-top: 10px;">Oops! Something went wrong. Please try again later</p>');
                        alert('Failed to send Email');
                    }
                }
            });
        }
    })
});
</script>